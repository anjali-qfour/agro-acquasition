function add_nav_home_btn(selected)
{
	
	var class_name = "nav_home_btn";
	
	$(" <div class='nav_btn flat_btn "+class_name+" '> "+	
			
	"</div>")
		.css({
			left:(0),
			top: (0),
			
		})
		.data("view_status",1)
		.click(function(){
		
		})		
		.appendTo(".nav_tool");
	
	
	
	

	

	var _draw_nav_btn = function ()
	{
		$.get("content/svg/home_btn.svg", function(text_data){
						
			
			d3.select(".nav_home_btn")
					.append("svg")
					.attr("height",lu/3)
					.attr("width",lu/3)
					.attr("preserveAspectRatio","xMidYMid")
					.append("g")					
					.html(text_data)
					.attr("transform","translate("
							+( 0 )+", "
							+( 0 )								
							+" ) scale("+(btn_scale)+")")	
					
					.on("click", function(d) {
						
						window.location ="user/overview";

					})					
					.on("mouseenter",function(d,e,g){
                        
                        draw_svg_tooltip(("Home"),d3.select("."+class_name+" svg"),[su_2,4]);
                      
                        d3.select(this).attr("fill-opacity",1);
		    	  

					})
					.on("mouseleave",function(e){
						d3.select(this).attr("fill-opacity",0.95);
						remove_tooltip();
					})
					;

					;
			
			if (selected){

				
				
				d3.select(".nav_home_btn .back_circle")										
					.attr("fill",colors.focus_btn_bg)
					.attr("stroke",colors.focus_btn_bg)
					;
				
				d3.select(".nav_home_btn .symb_g")					
    				.selectAll("*")
    				.attr("fill",colors.focus_btn_fg)
    				.attr("stroke",colors.focus_btn_fg)
    				;
				
			};
			
			
			
			
		}, "text");	
	};
	
	_draw_nav_btn();

	
	

}



